<template>
  <v-container>
    <h1 class="text-center">HOSTS</h1>
    <v-row>
      <v-col>
        <v-card>
          <v-card-title>My Hosts</v-card-title>
          <v-simple-table fixed-header>
            <template v-slot:default>
              <thead>
              <tr>
                <th class="text-left">Host</th>
                <th class="text-left">Description</th>
                <th class="text-left">OS</th>
              </tr>
              </thead>
              <tbody>
              <tr v-for="item in hosts" :key="item.name">
                <td>{{ item.name }}</td>
                <td>{{ item.description }}</td>
                <td>{{ item.os }}</td>
              </tr>
              </tbody>
            </template>
          </v-simple-table>
        </v-card>
      </v-col>
    </v-row>
    <v-row>
      <v-container>
        <v-btn color="success">Register New Host</v-btn>
      </v-container>
    </v-row>
  </v-container>
</template>

<script>

import JobbyApi from "@/services/JobbyApi";

export default {
  name: 'Home',
  data() {
    return {
      hosts: [],
    }
  },
  created() {
    this.fetchData();
  },
  methods: {
    fetchData: function () {
      JobbyApi.listHosts().then((data) => {
        this.hosts = data;
      })
    }
  },
}
</script>
